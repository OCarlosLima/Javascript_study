// Comando For repete alguma coisa um certo número de vezes
var numero = 5; // Quantidade de vezes que o For irá repetir
for (qntvez = 1; qntvez <= numero; qntvez++) {
    console.log("Executando pela vez número: " + qntvez);
}
console.log("Acabou");

