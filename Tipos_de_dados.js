// Boolean
// Valor logico
var a = true;
var b = false;
console.log(a == b);

// Null
// Sem valor dentro
var nula = null;
console.log(nula);

// Undefined
// Valor indefinido/vazio
var und = undefined;
console.log(und);

// Number
// Numero, inteiro ou flutuante
var numb = 10;
console.log(numb);

// String
// Cadeia de caracteres, letra ou texto
var str = "Aqui jas uma palavra";
console.log(str)