// Operador de condição mais resumido do que IF e ELSE
// Ultiliza-se ? para verificar se é verdade, e : para se é mentira (if else)
var idade = 21;
idade >= 18 ? console.log("Pode comprar") : console.log("Não pode comprar")

console.log("---------------------"); // DIVISÃO

// Pode usar para guardar um valor dentro de uma variável
var idade = 15;
var permitido = idade >= 18 ? true : false;
console.log(permitido) // RESULTADO DA CONDIÇÃO